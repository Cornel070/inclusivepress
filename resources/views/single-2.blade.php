                                    <h1 class="entry-title typescale-3_2">How Otukpo became an oasis of peace in Benue after Fulani militia’s attack</h1>
                                    <div class="entry-teaser">
                                    Thirteen days to the election that brought in Muhammadu Buhari as  president in 2015, Fulani ethnic militia killed about 90 persons in an overnight raid on Egba, a community in Otukpo, Benue State. Children, women, and men were murdered as the invaders burned down their houses.</div>
